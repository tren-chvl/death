off_t	vaddr_to_file_offset32(int fd, uint32_t vaddr)
{
	Elf32_Ehdr	head;
	Elf32_Phdr	seg;

	lseek(fd ,0 ,SEEK_SET);
	read(fd, &head, sizeof(head));
	for (int i = 0; i < head.e_phnum;i++)
	{
		lseek(fd, head.e_phoff + i * sizeof(seg), SEEK_SET);
		read(fd, &seg, sizeof(seg));
		if (seg.p_type == PT_LOAD &&
			vaddr >= seg.p_vaddr &&
			vaddr < seg.p_vaddr + seg.p_memsz)
		{
			return seg.p_offset + (off_t)(vaddr + - seg.p_vaddr);
		}
	}
	return -1;
}

